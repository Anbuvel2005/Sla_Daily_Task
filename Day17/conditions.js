// Create a switch to print days of the week.

let n = "monday";

switch (n) {
  case "monday":
    console.log("Monday");
    break;

  case "tuesday":
    console.log("Tuesday");
    break;

  case "wednesday":
    console.log("Wednesday");
    break;

  case "thursday":
    console.log("Thursday");
    break;

  case "friday":
    console.log("Friday");
    break;

  case "saturday":
    console.log("Saturday");
    break;

  case "sunday":
    console.log("Sunday");
    break;

  default:
    console.log("Invalid day");
}
